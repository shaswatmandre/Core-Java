package in.co.basics;

public class MethodCreation {

	public void add() {
		int a = 10;
		int b = 20;
		System.out.println("add:- " + (a + b));

	}

	public void sub() {
		int c = 50;
		int d = 20;
		 System.out.println("sub:- " + (c - d));

	}

	public void div() {
		int e = 100;
		int f = 10;
		System.out.println("div:- " + (e / f));

	}

	public void mul() {
		int g = 10;
		int h = 3;
		System.out.println("mul:- " + (g * h));

	}

}
