package in.co.basics;

public class ConstRectangle {
	
	private int length;
	private int width;
	public int getLength() {
		return length;
	}
	public int getWidth() {
		return width;
	}
	public ConstRectangle(int length, int width) {
		super();
		this.length = length;
		this.width = width;
	}
	
	

}
