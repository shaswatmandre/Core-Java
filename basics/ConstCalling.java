package in.co.basics;

public class ConstCalling {
	 private String name;
	 private int address;
	public ConstCalling(String name, int address) {
		super();
		this.name = name;
		this.address = address;
	}
	public String getName() {
		return name;
	}
	public int getAddress() {
		return address;
	}
	 
}
